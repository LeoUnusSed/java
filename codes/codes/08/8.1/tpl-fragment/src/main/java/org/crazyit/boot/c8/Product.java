package org.crazyit.boot.c8;

public class Product {

    private String name;
    
    public Product() {
        
    }

    public Product(String name) {
        super();
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
